package modelo;

public class Persona {
    String nombre;
    String direccion;
    String dni;
    String telefono;

    public String getNombre() {
        return nombre;
    }

    public String getDireccion() {
        return direccion;
    }

    public String getDni() {
        return dni;
    }

    public String getTelefono() {
        return telefono;
    }
}
